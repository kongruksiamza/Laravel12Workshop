     */
    public function store(Request $request)
    {
        $product = new Product([
          'name' => $request->get('name'),
          'price' => $request->get('price'),
          'type_id'=>$request->get('typename')
        ]);
        $product->save();
        return redirect()->route('product.index');
    }

